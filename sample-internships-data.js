// Helper function to get internships by department
export const getInternshipsByDepartment = (department) => {
  return sampleInternships.filter(internship => 
    internship.department.toLowerCase().includes(department.toLowerCase())
  );
};

// Helper function to get internships by type
export const getInternshipsByType = (type) => {
  return sampleInternships.filter(internship => 
    internship.type === type
  );
};

// Helper function to get remote internships
